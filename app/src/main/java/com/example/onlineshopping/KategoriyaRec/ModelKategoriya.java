package com.example.onlineshopping.KategoriyaRec;

public class ModelKategoriya {
    private int id;
    private String nomi;
    private byte[] rasmi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomi() {
        return nomi;
    }

    public void setNomi(String nomi) {
        this.nomi = nomi;
    }

    public byte[] getRasmi() {
        return rasmi;
    }

    public void setRasmi(byte[] rasmi) {
        this.rasmi = rasmi;
    }
}
